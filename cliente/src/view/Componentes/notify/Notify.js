import { toast } from "react-toastify";

const notify = (suffix, identificacion = "", tipo) => {
  if (tipo === "info") {
    toast.info(`${suffix} ${identificacion}`, {
      position: "top-center",
      autoClose: 4000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
    });
  } else {
    toast.error(`${suffix} ${identificacion}`, {
      position: "top-center",
      autoClose: 4000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
    });
  }
};

export { notify };
